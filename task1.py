subjects=["Math", "Physics", "Chemistry", "Biology", "CS"]

subjects.append("Bible knowledge") # This code add Bible knowledge in the subject list
print(subjects)

subjects.insert(2,"Geography") # this code insert Geography into index 2 of the list
print(subjects)

subjects.remove("Math") # this code removes Math from the list
print(subjects)

subjects.pop(2) # This code delete the item which is in index 2 from the subject list
print(subjects)

subjects.sort() # This code arranges the items in the list following the ascending order alphabetically
print(subjects)

subjects.reverse() # This code changes the arrangements of items in the list
print(subjects)